    public Consultation getParams() throws Exception {
        Consultation consultation = new Consultation();

        consultation.setPatient(patientHelper.getSelected());
        consultation.setDateConsultation((java.sql.Date) convertUtilToSql(jDateChooser1.getDate()));
        consultation.setHeure((int) (jSpinner1.getValue()));
        consultation.setMinute((int) jSpinner2.getValue());
        consultation.setTaille(new Integer(jTextField2.getText()));
        consultation.setPoids(new Integer(jTextField4.getText()));
        consultation.setTemperature(new Integer(jTextField3.getText()));
        //consultation.setLieuConsultation(jComboBox1.getSelectedItem()+"");
        consultation.setLieuConsultation(jTextField9.getText());
        consultation.setMotifconsultation(jTextArea1.getText());
        consultation.setMontantTotal(new Double(jTextField7.getText()));
        consultation.setMontantPayé(new Double(jTextField8.getText()));
        consultation.setTensionMin(new Integer(jTextField5.getText()));
        consultation.setTensionMax(new Integer(jTextField6.getText()));

        if (jRadioButton1.isSelected()) {
            consultation.setType("periodique");
        }
        if (jRadioButton2.isSelected()) {
            consultation.setType("Urgente");
        }

        if (jRadioButton5.isSelected()) {
            consultation.setEtatPaiement("Partiellement Payée");
        }
        if (jRadioButton4.isSelected()) {
            consultation.setEtatPaiement("Non Payée");
        }
        if (jRadioButton3.isSelected()) {
            consultation.setEtatPaiement("Payée");
        }

        return consultation;

    }

    private java.util.Date convertUtilToSql(java.util.Date utilDate) {

        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());

        return sqlDate;
    }

