    private void injectParam(Consultation consultation) {
        jTextField1.setText(consultation.getPatient().getPrenom());
        jDateChooser1.setDate(consultation.getDateConsultation());
        jSpinner1.setValue(consultation.getHeure());
        jSpinner2.setValue(consultation.getMinute());
        jTextField2.setText(consultation.getTaille() + "");
        jTextField3.setText(consultation.getTemperature() + "");
        jTextField4.setText(consultation.getPoids() + "");
        jTextField9.setText(consultation.getLieuConsultation());
        jTextArea1.setText(consultation.getMotifconsultation());
        jTextField7.setText(consultation.getMontantTotal() + "");
        jTextField8.setText(consultation.getMontantPayé() + "");
        jTextField5.setText(consultation.getTensionMin() + "");
        jTextField6.setText(consultation.getTensionMax() + "");
        
        
        if(consultation.getType().equals("périodique"))
        {
            jRadioButton1.setSelected(true);
        }
        if(consultation.getType().equals("urgente"))
        {
            jRadioButton2.setSelected(true);
        }
        
        if(consultation.getEtatPaiement().equals("Partiellement Payée"))
        {
            jRadioButton5.setSelected(true);
        }
        if(consultation.getType().equals("Non Payée"))
        {
            jRadioButton4.setSelected(true);
        }
        if(consultation.getType().equals("Payée"))
        {
            jRadioButton3.setSelected(true);
        }
        
        
        
    }

